# WORKOUT PLAN PDF
# ─────────────────────────────────────────────
def generate_workout_plan_pdf(user_profile: dict, workout_plan: dict) -> bytes:
    """
    Generate a styled PDF for the 7-day workout plan.
    Returns PDF as bytes.
    """
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=A4,
        topMargin=2 * cm,
        bottomMargin=1.5 * cm,
        leftMargin=1.5 * cm,
        rightMargin=1.5 * cm,
    )
    styles = _build_styles()
    story = []

    story.append(Spacer(1, 1 * cm))
    story.append(Paragraph("🏋️ Personalized 7-Day Workout Plan", styles["title"]))
    story.append(
        Paragraph(
            f"Prepared for: <b>{user_profile.get('name', 'User')}</b>  |  Goal: {user_profile.get('goal', 'N/A')}  |  Level: {user_profile.get('activity_level', 'N/A')}",
            styles["subtitle"],
        )
    )
    story.append(HRFlowable(width="100%", thickness=1, color=GREEN, spaceAfter=12))

    days_data = workout_plan.get("days", workout_plan) if isinstance(workout_plan, dict) else {}

    for day_key, day_info in days_data.items():
        story.append(Paragraph(f"📅 {day_key}", styles["section_header"]))
        if isinstance(day_info, dict) and day_info.get("rest_day"):
            story.append(Paragraph("😴 Rest Day — Focus on recovery and hydration.", styles["body"]))
        else:
            exercises = day_info.get("exercises", []) if isinstance(day_info, dict) else []
            table_data = [["Exercise", "Sets", "Reps", "Rest", "Muscle Group", "Calories"]]
            for ex in exercises:
                if isinstance(ex, dict):
                    exercise_name = ex.get("name", "")
                    table_data.append([
                        Paragraph(str(exercise_name), styles["cell_body"]),
                        str(ex.get("sets", "")),
                        str(ex.get("reps", "")),
                        ex.get("rest", ""),
                        ex.get("muscle_group", ""),
                        str(ex.get("calories_burned", "")),
                    ])
            if len(table_data) > 1:
                t = Table(table_data, colWidths=[2.5*inch, 0.7*inch, 0.7*inch, 0.9*inch, 1.5*inch, 0.8*inch])
                t.setStyle(_table_style())
                story.append(t)
        story.append(Spacer(1, 0.3 * cm))

    doc.build(story, onFirstPage=_header_footer, onLaterPages=_header_footer)
    return buffer.getvalue()


# ─────────────────────────────────────────────
# WEEKLY REPORT PDF
# ─────────────────────────────────────────────
def generate_weekly_report_pdf(user_profile: dict, report: dict) -> bytes:
    """
    Generate a styled PDF for the weekly health report.
    Returns PDF as bytes.
    """
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=A4,
        topMargin=2 * cm,
        bottomMargin=1.5 * cm,
        leftMargin=1.5 * cm,
        rightMargin=1.5 * cm,
    )
    styles = _build_styles()
    story = []

    story.append(Spacer(1, 1 * cm))
    story.append(Paragraph("📊 Weekly Health & Fitness Report", styles["title"]))
    story.append(
        Paragraph(
            f"User: <b>{user_profile.get('name', 'User')}</b>  |  Week ending: {datetime.now().strftime('%d %b %Y')}",
            styles["subtitle"],
        )
    )
    story.append(HRFlowable(width="100%", thickness=1, color=GREEN, spaceAfter=12))

    # Performance Score
    score = report.get("performance_score", "N/A")
    story.append(Paragraph(f"🏆 Overall Performance Score: <b>{score} / 100</b>", styles["section_header"]))
    story.append(Spacer(1, 0.3 * cm))

    # Achievements
    story.append(Paragraph("✅ Top Achievements", styles["section_header"]))
    for ach in report.get("achievements", []):
        story.append(Paragraph(f"• {ach}", styles["body"]))
    story.append(Spacer(1, 0.3 * cm))

    # Areas to Improve
    story.append(Paragraph("⚠️ Areas to Improve", styles["section_header"]))
    for imp in report.get("improvements", []):
        story.append(Paragraph(f"• {imp}", styles["body"]))
    story.append(Spacer(1, 0.3 * cm))

    # Personalized Advice
    story.append(Paragraph("💡 Personalized Advice for Next Week", styles["section_header"]))
    advice = report.get("advice", "")
    story.append(Paragraph(advice, styles["body"]))
    story.append(Spacer(1, 0.3 * cm))

    # Motivational Message
    story.append(Paragraph("🌟 Motivational Message", styles["section_header"]))
    motivation = report.get("motivational_message", "Keep going — every step counts!")
    story.append(Paragraph(f"<i>{motivation}</i>", styles["body"]))

    doc.build(story, onFirstPage=_header_footer, onLaterPages=_header_footer)
    return buffer.getvalue()
